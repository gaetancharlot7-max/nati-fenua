def get_translation(key: str, lang: str = "fr", **kwargs) -> str:
    """Get a translation for a key in the specified language"""
    if key not in TRANSLATIONS:
        return key
    
    translation = TRANSLATIONS[key].get(lang, TRANSLATIONS[key].get("fr", key))
    
    # Replace placeholders like {n}
    for k, v in kwargs.items():
        translation = translation.replace(f"{{{k}}}", str(v))
    
    return translation


def get_all_translations(lang: str = "fr") -> dict:
    """Get all translations for a language"""
    return {key: trans.get(lang, trans.get("fr", key)) for key, trans in TRANSLATIONS.items()}
